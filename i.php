<h3>Indomethacin Drug Information</h3>
<h4>Indomethacin Overview</h4>

Indomethacin 25mg is used to relieve pain, swelling, and joint stiffness caused by arthritis, gout, bursitis, and tendonitis. Reducing these symptoms helps you do more of your normal daily activities. Indomethacin 25mg is known as a nonsteroidal anti-inflammatory drug (NSAID). If you are treating a chronic condition such as arthritis, ask your doctor about non-drug treatments and/or using other medications to treat your pain. See also Warning section.

<h4>How To Use Indomethacin</h4>

Take Indomethacin 25mg by mouth as directed by your doctor, usually once or twice daily with a full glass of water (8 ounces or 240 milliliters). Do not lie down for at least 10 minutes after taking Indomethacin 25mg. Take Indomethacin 25mg either with food, right after meals, or with antacids to prevent stomach upset.

<h4>Indomethacin Precautions</h4>

Before taking Indomethacin 25mg 25mg, tell your doctor or pharmacist if you are allergic to it; or to aspirin or other NSAIDs (such as ibuprofen, naproxen, celecoxib); or if you have any other allergies. This product may contain inactive ingredients, which can cause allergic reactions or other problems. Before using Indomethacin 25mg, tell your doctor or pharmacist your medical history, especially of: asthma (including a history of worsening breathing after taking aspirin or other NSAIDs), bleeding or clotting problems, dehydration, growths in the nose (nasal polyps), heart disease (such as congestive heart failure, previous heart attack), high blood pressure, kidney disease, liver disease, stomach/intestinal/esophagus problems (such as bleeding, ulcers, recurring heartburn), stroke.

<h4>Indomethacin Misdose</h4>

If you are prescribed Indomethacin 25mg on a regular schedule (not just "as needed") and you miss a dose, take it as soon as you remember. If it is near the time of the next dose, skip the missed dose and resume your usual dosing schedule. Do not double the dose to catch up.

<h4>Indomethacin Interactions</h4>

Some products that may interact with Indomethacin 25mg include: cidofovir, cyclosporine, lithium, methotrexate, pemetrexed, "water pills" (diuretics such as furosemide, hydrochlorothiazide, triamterene), high blood pressure drugs (including ACE inhibitors such as captopril, lisinopril and angiotensin II receptor blockers such as losartan, valsartan).

<h4>Indomethacin Side Effects</h4>

Upset stomach, heartburn, headache, tiredness, drowsiness, or dizziness may occur. If any of these effects persist or worsen, tell your doctor or pharmacist promptly.

<h4>Indomethacin Storage</h4>

Store at room temperature away from light and moisture. Do not store in the bathroom. Keep all medications away from children and pets. Do not flush medications down the toilet or pour them into a drain unless instructed to do so. Properly discard this product when it is expired or no longer needed. Consult your pharmacist or local waste disposal company.
